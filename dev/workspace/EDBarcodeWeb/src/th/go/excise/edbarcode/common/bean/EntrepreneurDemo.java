package th.go.excise.edbarcode.common.bean;

public class EntrepreneurDemo {
	
	private String licenseNo;
	private String licenseAllowedName;
	private String factoryName;
	private String licenseStartDate;
	private String licenseEndDate;
	private String taxNo;
	private String factoryAddress;

	public String getLicenseNo() {
		return licenseNo;
	}

	public void setLicenseNo(String licenseNo) {
		this.licenseNo = licenseNo;
	}

	public String getLicenseAllowedName() {
		return licenseAllowedName;
	}

	public void setLicenseAllowedName(String licenseAllowedName) {
		this.licenseAllowedName = licenseAllowedName;
	}

	public String getFactoryName() {
		return factoryName;
	}

	public void setFactoryName(String factoryName) {
		this.factoryName = factoryName;
	}

	public String getLicenseStartDate() {
		return licenseStartDate;
	}

	public void setLicenseStartDate(String licenseStartDate) {
		this.licenseStartDate = licenseStartDate;
	}

	public String getLicenseEndDate() {
		return licenseEndDate;
	}

	public void setLicenseEndDate(String licenseEndDate) {
		this.licenseEndDate = licenseEndDate;
	}

	public String getTaxNo() {
		return taxNo;
	}

	public void setTaxNo(String taxNo) {
		this.taxNo = taxNo;
	}

	public String getFactoryAddress() {
		return factoryAddress;
	}

	public void setFactoryAddress(String factoryAddress) {
		this.factoryAddress = factoryAddress;
	}
	
}
